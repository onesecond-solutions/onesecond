소비자 보호를 위한 유의사항
영업행위 
全
 과정에서 윤리적인 의사결정과 행동으로 보험소비자보호를 위해 항상 노력하며 다음의 원칙을 적극 실천한다.
신한라이프 보험소비자를 위한 영업윤리준칙
1. 신의성실 원칙
· 보험소비자가 계약과정에서 오해가 없도록 노력하며 정당한 계약을 체결한다.
· 보험소비자의 약속은 반드시 지키며 피해가 발생한 경우 신속하게 구제한다.
2. 소비자이익 최우선 원칙
· 판매자는 소비자이익에도 도움이 되도록 지속적인 학습을 통해 전문역량을 갖춘다.
· 보험소비자의 정보를 충분히 파악하여 소비자이익을 위한 최적의 상품을 추천한다.
3. 정보보호의 원칙
· 개인정보 수집과 활용 시 동의절차를 준수하며 목적에 부합되는 정보만 수집한다.
· 수집된 정보는 해당 목적에만 사용하고 목적 달성 시 폐기한다.
4. 금융취약계층 보호 원칙
· 장애인, 고령자, 외국인 등 취약한 소비자인 경우 부당한 차별이 없도록 설명한다.
· 상품의 중요사항을 더욱 신중하게 설명하고 이해 여부를 확인한다. 
5. 완전판매의 원칙
· 보험소비자가 상품의 중요사항을 이해할 수 있도록 누락 없이 실행한다.
· 상품에 대한 이해 여부를 확인하여야 하며 필요한 서류 등을 반드시 교부한다.
