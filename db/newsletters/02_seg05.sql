적합성 원칙
(법 제 17조)
일반 금융소비자에게 금융상품을 권유 · 판매하는 경우, 각 소비자의 정보를 정확히 파악하고 
적합한 상품만을 권유해야 합니다.
적정성 원칙
(법 제 18조)
금융소비자가 권유받지 않고, 자발적으로 금융 상품을 구매한다고 하더라도 판매자는 
해당 금융 상품이 소비자에게 적정한 상품인지 판단하고, 이를 고지해야 합니다.
설명 의무
(법 제 19조)
금융상품을 권유하거나 금융소비자가 설명을 요청하는 경우, 
해당 상품에 대해 자세히 설명하고 자필 서명을 받아야 합니다.
불공정 영업행위 금지
(법 제 20조)
다른 금융상품을 가입하게 하거나 부당한 추가 담보 또는 연대보증 등을 요구하는 등의 
불공정한 영업행위를 금지하고 있습니다.
부당 권유 행위 금지
(법 제 21조)
금융상품 권유 시, 불확실한 사항에 대해 단정적으로 판단하거나 
금융상품의 내용을 실제 사실과 달리 설명하는 등 오인할 가능성이 있는 허위사실 등을 절대 알려서는 안됩니다.
허위 과장 광고 금지
(법 제 22조)
금융상품 또는 판매업자 등의 업무에 관한 광고 시, 허위 및 과장 된 광고를 금지하며,
중요사항을 반드시 포함해야 하며, 투자 손실이 보전되는 것과 같은 오해를 유발할 수 있는 행위 등을 금지하고 있습니다.
