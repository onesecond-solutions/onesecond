해당 질병이력으로는 간편심사형 가입이 불가합니다. 다만, 신용정보원(ICIS) 지급이력을 바탕으로 확인한 내용이므로 해당 지급이력 정보가 틀린 경우에 한하여 선심사 진행을 통해 인심사 요
청이 가능합니다.
12최종심사결과
최종심사결과를 확인하고 바로 청약입력하지 않으면 청약입력 시 다시 선심사를 진
행해야 하나요?
최종심사결과가 ''인수''이면 ''선심사진행현황'' 조회를 통해 72시간 동안 선심사 재수행없이 청약입력이 가능합니다.
(*72시간 : 영업일기준)
13최종심사결과
최종심사결과에서 가입설계한 특약들 중에 심사결과가 안보이는 특약이 있어요 어
떤 기준으로 보여지는 건가요?
심사결과 ''인수''인 특약은 심사결과에 표출되지 않습니다. 다만, 주계약의 경우 ''인수'' 이더라도 표출됩니다.
14선심사결과반영하기 ''선심사결과 반영하기'' 버튼을 클릭하면 어떻게 되나요?
선심사 or 인심사 결과 ''인수'' 혹은 ''조건부 인수''가 나오면 해당 심사결과를 반영하여 청약진행이 가능합니다.
조건부 인수의 경우 각 심사결과에 따라 반영되는 내용은 사용자 매뉴얼을 통해 상세히 확인할 수 있습니다.
15인심사진행 중인 인심사 건이 있는데 다시 가입설계하여 선심사 재진행이 가능한가요?다시 설계하여 선심사 재진행이 가능합니다.
16질병심사시스템추가질문
질병이력을 자동으로 불러왔는데 고지가 완료되지 않습니다. 무엇을 더 작성해야
하나요?
자동셋팅된 질병이력 정보는 신용정보원(ICIS) 지급이력에서 확인할 수 있는 정보로 제한적입니다.
사용자의 고지 편의를 위해 자동셋팅 되나 최종적인 심사요청을 위해서는 완치여부, 입원수술력, 추가질문에 대한 고지까지 완료되어야 합니다.
17추가고지 버튼고지완료 메시지에서 ''추가고지'' 버튼이 있습니다. 이 버튼은 무슨 기능인가요?
필수고지 이력 외에 계약전 알릴의무에 해당하는 질병이력이 있다면 ''추가고지'' 버튼을 통해 고지할 수 있습니다.
단, 보험금 수령이력이 있는 질병이력은 추가고지 하지 않아도 됩니다. (보험금 수령한 질병이력 = 자동심사 진행됨)
*추가고지 버튼 클릭 > 질병고지 추가 버튼 클릭 > 질병명, 질병이력 입력 > 고지완료
18청약입력청약서발행청약서를 발행한 후 변경할 고지 내용이 발생했습니다. 수정이 가능한가요?청약서 발행 전 고지내용으로 이미 심사가 이루어져 청약이 진행되고 있는 상황이므로 고지를 수정하고자 하시는 경우 선심사를 재진행해 주셔야 합니다.
19고지내용확인
기존에는 청약서에 고지를 작성하고 스캔하였는데 가입설계 시 고지하면 어떻게 달
라지는 건가요?
보험료 입금 전 질병고지하여 선심사를 진행했기 때문에 청약서 발행 시 선심사에서 고지한 내용이 입력되어 나옵니다.
해당 고지내용을 확인하시고, 고지 이상없음을 자필서명 하면 됩니다.
다만, 실제와 다른 내용이 있을 경우 고지를 다시하여 선심사 재진행 후 청약해 주시기 바랍니다.
20청약접수선심사 도입 이후 청약접수 시 달라지는 점이 있나요?
계약전 알릴의무를 가입설계에서 진행하는 것을 제외하고는 기존과 달라지는 점은 없습니다.
기존에는 청약진행 과정에서 계약전 알릴의무를 고지하고 자필서명 하였지만
선심사 도입 후에는 가입설계 과정에서 계약전 알릴의무를 고지하고 청약진행 과정에서는 해당 고지내용을 청약서에서 다시 확인하고 자필서명하는 절차를 거칩니다.
21승낙/반송승낙/반송선심사결과 인수였는데 청약 후 거절이 되기도 하나요?
선심사는 질병이력에 대한 사항만 심사가 되는 것으로 이외 재정, AML, 외국인 등의 심사가 필요한 경우 청약 이후 거절 될 수도 있습니다.
또한 진단, 적부는 청약 후 후심사로 진행되기 때문에 진단 / 적부 결과에 따라 청약이 거절 될 수도 있습니다.
22선심사 진행현황선심사
심사결과를 산출한 이후 특약추가등 수정사항이 있을 경우 다시 선심사 요청을 해
야 하나요?
보장금액을 감액하거나, 선택한 특약을 빼는 경우 산출된 선심사결과로 진행 가능합니다.
단, 보장금액을 증액하거나, 특약을 추가한 경우 선심사를 재진행 하셔야 합니다.
23인심사인심사 요청건은 언제까지 인심사 결과를 확인할 수 있나요?
언더라이터는 인심사 및 청약심사도 병행하여 진행하고 있어 인심사 접수 건은 4시까지만 답변됩니다.
단, 신계약 마감일에는 3시까지 답변됩니다. (인심사 4시까지 의뢰된 건이 아닌 점 착오 없으시길 바랍니다.)
24인심사인심사 보완 요청을 받은 경우 보완 기일이 있나요?
보완 요청 받은 일자를 기준으로 7일 이내 완료되어야 하며, 보완 완료 시 인심사가 재개됩니다.
기일 내 보완하지 않은 경우 해당 선심사건은 활용 불가합니다.
25인심사휴일에 인심사를 요청한 건은 결과를 언제 받을 수 있나요?휴일에 인심사 요청이 된 건은 익영업일에 인심사됩니다.
26선심사 제외 대상(후심사)청약심사
선심사에서 인수가능하다 결과 받았는데 청약진행 하니 다시 심사중으로 확인됩니
다. 왜 그런건가요?
선심사를 통해서는 건강력에 대한 심사결과만 확인할 수 있습니다. 진단, 적부, 재정 등의 사유로 자동성립 되지 않은 건은 보험료 입금 후 청약심사로 진행됩니다.
27신용정보원 지급이력 오류필수고지 및 가입불가 질환
고객이 고지한 내역과 상이한 지급이력이 뜹니다. 선심사를 어떻게 진행하면 되나
요?
신정원의 지급이력은 고객이 해당 보험사를 통해서만 수정할 수 있습니다. 일단은 추가고지 또는 상세고지를 통해 고객의 고지내용을 반영하여 선심사를 진행해 주십시오.');
