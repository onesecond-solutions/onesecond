--   owner=임태성(98c5f4f9-10c1-4ee1-a656-5c2ca63239fd) 살아있는 고객 = 1000명,
--   전원 status='신규'·2026-06 생성·profile 없음 = 옛 일괄/데모 데이터.
--   대표가 폼으로 직접 추가한 고객(profile 有/8단계 status/7월+) = 0명. 휴지통(deleted_at 有) = 0명.
--   대표 지시: "전부 다 비워, 상담관리 화면 제로 베이스에서 다시 시작."
--
-- 성격: 소프트삭제(deleted_at)만 설정 → 하드 DELETE 아님. 휴지통 화면 [복구] 또는 아래 DOWN으로 되돌리기 가능.
--   ⚠️ 다른 owner 데이터·다른 테이블 무접촉. WHERE로 owner=임태성 + 아직 안 지워진 것만 한정.
-- 멱등성: deleted_at is null 조건 → 재실행해도 이미 지워진 행 재삭제 안 함(대상 0건).
-- ═══════════════════════════════════════════════════════════════════════════

begin;

update sales_customers
   set deleted_at = now()
 where owner_id = '98c5f4f9-10c1-4ee1-a656-5c2ca63239fd'
   and deleted_at is null;

commit;

